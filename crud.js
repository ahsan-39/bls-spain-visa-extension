let form = document.getElementById("form");
let textInput = document.getElementById("textInput");
let dateInput = document.getElementById("dateInput");
let textarea = document.getElementById("textarea");
let msg = document.getElementById("msg");
let tasks = document.getElementById("tasks");
let add = document.getElementById("add");
// let btn_edit = document.getElementById("btn_edit");
// let btn_delete = document.getElementById("btn_delete");

let btn_edit = '';
let btn_delete = '';

form.addEventListener("submit", (e) => {
  e.preventDefault();
  formValidation();
});


let formValidation = () => {
  if (textInput.value === "") {
    console.log("failure");
    msg.innerHTML = "Task cannot be blank";
  } else {
    console.log("success");
    msg.innerHTML = "";
    acceptData();
    add.setAttribute("data-bs-dismiss", "modal");
    add.click();

    (() => {
      add.setAttribute("data-bs-dismiss", "");
    })();
  }
};

let data = [{}];

let acceptData = () => {
  data.push({
    text: textInput.value,
    date: dateInput.value,
    description: textarea.value,
  });

  localStorage.setItem("data", JSON.stringify(data));

  console.log(data);
  createTasks();
};

let createTasks = () => {
  tasks.innerHTML = "";
  data.map((x, y) => {
    return (tasks.innerHTML += `
    <div id=${y}>
          <span class="fw-bold">${x.text}</span>
          <span class="small text-secondary">${x.date}</span>
          <p>${x.description}</p>
  
          <span class="options">
            <i data-index="${y}" data-bs-toggle="modal" data-bs-target="#form" class="fas fa-edit btn_edit"></i>
            <i data-index="${y}" class="fas fa-trash-alt btn_delete"></i>
          </span>
        </div>
    `);
  });

  resetForm();

  btn_edit = document.getElementsByClassName("btn_edit");
  btn_delete = document.getElementsByClassName("btn_delete");
};

let deleteTask = (e) => {
  e.parentElement.parentElement.remove();
  data.splice(e.parentElement.parentElement.id, 1);
  localStorage.setItem("data", JSON.stringify(data));
  console.log(data);
  //createTasks();
};

let editTask = (e) => {
  let selectedTask = e.parentElement.parentElement;

  textInput.value = selectedTask.children[0].innerHTML;
  dateInput.value = selectedTask.children[1].innerHTML;
  textarea.value = selectedTask.children[2].innerHTML;

  deleteTask(e);
};

let resetForm = () => {
  textInput.value = "";
  dateInput.value = "";
  textarea.value = "";
};

(() => {
  data = JSON.parse(localStorage.getItem("data")) || []
  console.log(data);
  createTasks();
})();

if(btn_delete){
  btn_delete.addEventListener("click", (e) => {
    e.preventDefault();
    let index = $(this).data('index');
    //delete data[];
    console.log(e,index);
    deleteTask(e);
  });
}
